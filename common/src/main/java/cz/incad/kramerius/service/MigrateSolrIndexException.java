package cz.incad.kramerius.service;

public class MigrateSolrIndexException extends Exception {

    public MigrateSolrIndexException() {
        super();
    }

    public MigrateSolrIndexException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
        super(arg0, arg1, arg2, arg3);
    }

    public MigrateSolrIndexException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public MigrateSolrIndexException(String arg0) {
        super(arg0);
    }

    public MigrateSolrIndexException(Throwable arg0) {
        super(arg0);
    }
}
