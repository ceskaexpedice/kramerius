class LexerException extends Exception {

	/**
	 * 
	 */
	LexerException() {
		super();
		
	}
	/**
	 * @param message
	 */
	LexerException(String message) {
		super(message);
		
	}
	/**
	 * @param message
	 * @param cause
	 */
	LexerException(String message, Throwable cause) {
		super(message, cause);
		
	}
	/**
	 * @param cause
	 */
	LexerException(Throwable cause) {
		super(cause);
		
	}
}